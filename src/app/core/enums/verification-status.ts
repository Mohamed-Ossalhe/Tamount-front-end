/**
 * Enumeration representing the verification status of an entity.
 *
 * @author Mohamed Ossalhe
 */
export enum VerificationStatus {
	/**
	 * Indicates that the entity has been verified.
	 */
	VERIFIED = 'VERIFIED',

	/**
	 * Indicates that the entity has not been verified.
	 */
	NOT_VERIFIED = 'NOT_VERIFIED',
}
