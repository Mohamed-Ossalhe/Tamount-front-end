/**
 * Enum representing different preference statuses.
 *
 * @author Mohamed Ossalhe
 */
export enum PreferenceStatus {
	/**
	 * Represents a preference of "Yes".
	 */
	YES = 'YES',

	/**
	 * Represents a preference of "Maybe".
	 */
	MAYBE = 'MAYBE',

	/**
	 * Represents a preference of "No".
	 */
	NO = 'NO',
}
